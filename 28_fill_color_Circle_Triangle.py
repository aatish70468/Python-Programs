import turtle as t
t.penup()
t.setposition(100, 100)
t.pendown()
t.color("red")
t.begin_fill()
t.fd(80)
t.left(90)
t.fd(80)
t.left(90)
t.fd(80)
t.left(90)
t.fd(80)
t.end_fill()
t.penup()

t.setposition(-50, -50)
t.pendown()
t.color("red")
t.begin_fill()
t.fd(100)
t.right(120)
t.fd(100)
t.right(120)
t.fd(100)
t.end_fill()
t.penup()
t.setposition(-100, 100)
